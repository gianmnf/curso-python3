"""
Python = Linguagem de Programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas Simples
print('Gian Michel')
print(1,'Gian "Michel"')

# Aspas Duplas
print("Gian Michel")
print(2,"Gian 'Michel'")

# Escape
print("Gian \"Michel\"")

# r
print(r"Gian \"Michel\"")